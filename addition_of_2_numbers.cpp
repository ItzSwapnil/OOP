//
// Addition of 2 numbers with user input in c++ in expressive notation
//

#include <iostream>

int main() {
    int a, b;
    std::cout << "Enter first number:";
    std::cin >> a;
    std::cout << "Enter second number:";
    std::cin >> b;
    std::cout << "sum of the number " << a << " and " << b << " is " << a+b << std::endl;
    return 0;
}


/*
        Output
Enter first number:5

Enter second number:6

sum of the number 5 and 6 is 11

*/